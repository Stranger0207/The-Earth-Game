"""
کمک‌کننده‌های ظاهری (UI/UX): رنگ دکمه‌ها، نشانگر انتخاب و سربرگ‌های یکدست.

رنگ دکمه‌ها از طریق فیلد `style` در InlineKeyboardButton اعمال می‌شود (Bot API):
- 'success' → سبز (تأیید/مثبت)
- 'danger'  → قرمز (لغو/مخرب)
- 'primary' → آبی (کنش اصلی/ناوبری)
کلاینت‌هایی که این قابلیت را ندارند، دکمه را با ظاهر پیش‌فرض نشان می‌دهند؛ به همین
دلیل ایموجی‌ها هم به‌عنوان نشانه‌ی کمکی نگه داشته می‌شوند.
"""

from __future__ import annotations

# مقادیر مجاز فیلد style دکمه‌ها
STYLE_OK = "success"      # سبز — تأیید، پذیرش، خرید، احداث، انتخاب‌شده
STYLE_NO = "danger"       # قرمز — لغو، رد، حمله، وضع تحریم، عملیات مخرب
STYLE_MAIN = "primary"    # آبی — دکمه‌های اصلی منو و کنش اصلی هر صفحه

# نشانگر انتخاب در فهرست‌های چندانتخابی (روشن/خاموش)
PICK_ON = "🟢"
PICK_OFF = "⚪️"

# خط جداکننده‌ی تزئینی زیر سربرگ‌ها
DIVIDER = "➖➖➖➖➖➖➖➖➖➖"


def header(title: str, emoji: str = "") -> str:
    """یک سربرگ یکدست: عنوان درشت + خط جداکننده‌ی زیر آن."""
    prefix = f"{emoji} " if emoji else ""
    return f"{prefix}<b>{title}</b>\n{DIVIDER}"
