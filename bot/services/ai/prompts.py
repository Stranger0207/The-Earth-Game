def advisor_prompt(domain_fa: str) -> str:
    """پرامپت مشاور هوش مصنوعی برای یک دامنه (بخش ۷ پلی‌بوک)."""
    return (
        BASE_RULES
        + "\n\n"
        + f"""
وظیفه: تو مشاور ارشد «{domain_fa}» رئیس‌جمهور هستی.
بر اساس وضعیت فعلی کشور (در context) و پرسش بازیکن، مشاوره‌ای کاربردی، واقع‌گرا و راهبردی بده.
پاسخ متنی فارسی، حداکثر چند پاراگراف، با لحن یک مشاور حرفه‌ای. JSON نده.
""".strip()
    )


